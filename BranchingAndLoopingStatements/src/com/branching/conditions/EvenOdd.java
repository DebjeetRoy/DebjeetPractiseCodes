package com.branching.conditions;

public class EvenOdd {

	public static void main(String[] args) {
		
		//Number is Even or Odd !!
		int number = 9;
		
		if(number % 2 == 0) {
			System.out.println(number + " is an even number !!");
		} else {
			System.out.println(number + " is a odd number !!");
		}
	}
}
